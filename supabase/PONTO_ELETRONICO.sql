  id bigint generated by default as identity primary key,
  municipio_id bigint not null unique,
  tolerancia_atraso_minutos integer default 10,
  exigir_gps boolean default true,
  exigir_foto_entrada boolean default false,
  exigir_foto_saida boolean default false,
  raio_maximo_metros integer default 500,
  latitude_base numeric(10,7),
  longitude_base numeric(10,7),
  lancar_banco_horas boolean default true,
  criado_em timestamptz default now(),
  atualizado_em timestamptz default now()
);

