create or replace function public.atualizar_ponto_eletronico()
returns trigger
language plpgsql
as $$
declare
  minutos_total integer;
  minutos_previstos_calculados integer;
  atraso_calculado integer;
begin
  new.atualizado_em = now();

  if new.entrada_em is not null and new.saida_em is not null then
    minutos_total := greatest(
      0,
      floor(extract(epoch from (new.saida_em - new.entrada_em)) / 60)::integer
    );
    new.minutos_trabalhados := minutos_total;
    new.situacao := case
      when new.situacao = 'CANCELADO' then 'CANCELADO'
      else 'FECHADO'
    end;
  end if;

  if new.jornada_prevista_inicio is not null
     and new.jornada_prevista_fim is not null then
    minutos_previstos_calculados :=
      floor(
        extract(
          epoch from (
            (current_date + new.jornada_prevista_fim) -
            (current_date + new.jornada_prevista_inicio)
          )
        ) / 60
      )::integer;

    if minutos_previstos_calculados <= 0 then
      minutos_previstos_calculados := minutos_previstos_calculados + 1440;
    end if;

    new.minutos_previstos := minutos_previstos_calculados;
  end if;

  if new.entrada_em is not null and new.jornada_prevista_inicio is not null then
    atraso_calculado := greatest(
      0,
      floor(
        extract(
          epoch from (
            new.entrada_em -
            (new.data + new.jornada_prevista_inicio)
          )
        ) / 60
      )::integer - coalesce(new.tolerancia_minutos, 0)
    );
    new.minutos_atraso := atraso_calculado;
  end if;

  if new.minutos_trabalhados > 0 and new.minutos_previstos > 0 then
    new.minutos_extras := greatest(
      0,
      new.minutos_trabalhados - new.minutos_previstos
    );
    new.minutos_debito := greatest(
      0,
      new.minutos_previstos - new.minutos_trabalhados
    );
  end if;

  return new;
end;
$$;

drop trigger if exists trg_atualizar_ponto_eletronico
on public.ponto_eletronico;

create trigger trg_atualizar_ponto_eletronico
before insert or update on public.ponto_eletronico
for each row
execute function public.atualizar_ponto_eletronico();

drop trigger if exists trg_atualizar_ponto_configuracao
on public.ponto_eletronico_configuracao;

create or replace function public.atualizar_ponto_configuracao()
returns trigger
language plpgsql
as $$
begin
  new.atualizado_em = now();
  return new;
end;
$$;

create trigger trg_atualizar_ponto_configuracao
before update on public.ponto_eletronico_configuracao
for each row
execute function public.atualizar_ponto_configuracao();

