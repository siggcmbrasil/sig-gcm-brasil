  add column if not exists numero text,
  add column if not exists tipo text default 'SINDICANCIA',
  add column if not exists titulo text,
  add column if not exists assunto text,
  add column if not exists descricao text,
  add column if not exists origem text,
  add column if not exists natureza text,
  add column if not exists sigilo text default 'RESTRITO',
  add column if not exists prioridade text default 'NORMAL',
  add column if not exists status text default 'INSTAURADO',
  add column if not exists data_instauracao date,
  add column if not exists prazo_final date,
  add column if not exists portaria_numero text,
  add column if not exists autoridade_instauradora text,
  add column if not exists presidente_comissao text,
  add column if not exists membros_comissao text,
  add column if not exists guarda_id bigint,
  add column if not exists servidor_nome text,
  add column if not exists servidor_matricula text,
  add column if not exists conclusao text,
  add column if not exists decisao_final text,
  add column if not exists penalidade text,
