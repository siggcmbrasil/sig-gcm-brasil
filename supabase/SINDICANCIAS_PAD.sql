create table if not exists public.processos_disciplinares_movimentacoes (
  id bigint generated by default as identity primary key,
  municipio_id bigint not null,
  processo_id bigint not null references public.processos_disciplinares(id) on delete cascade,
  tipo text not null,
  titulo text not null,
  descricao text,
  data_movimentacao date not null default current_date,
  prazo_relacionado date,
  usuario_id bigint,
  usuario_nome text,
  criado_em timestamptz not null default now()
);

create table if not exists public.processos_disciplinares_envolvidos (
  id bigint generated by default as identity primary key,
  municipio_id bigint not null,
  processo_id bigint not null references public.processos_disciplinares(id) on delete cascade,
  guarda_id bigint,
  nome text not null,
  matricula text,
  papel text not null default 'INVESTIGADO',
  observacoes text,
  criado_em timestamptz not null default now()
);

create table if not exists public.processos_disciplinares_documentos (
  id bigint generated by default as identity primary key,
  municipio_id bigint not null,
  processo_id bigint not null references public.processos_disciplinares(id) on delete cascade,
  nome_arquivo text not null,
  url_arquivo text not null,
  tipo_documento text,
  sigiloso boolean not null default true,
  criado_por bigint,
  criado_em timestamptz not null default now()
);

create index if not exists idx_processos_disciplinares_municipio_status
  on public.processos_disciplinares (municipio_id, status);
create index if not exists idx_processos_disciplinares_prazo
  on public.processos_disciplinares (municipio_id, prazo_final);
create index if not exists idx_processos_disciplinares_guarda
  on public.processos_disciplinares (municipio_id, guarda_id);
create index if not exists idx_processos_movimentacoes_processo
  on public.processos_disciplinares_movimentacoes (processo_id, criado_em desc);

create or replace function public.atualizar_processo_disciplinar_timestamp()
returns trigger language plpgsql as $$
begin new.atualizado_em = now(); return new; end;
$$;

drop trigger if exists trg_processos_disciplinares_atualizado on public.processos_disciplinares;
create trigger trg_processos_disciplinares_atualizado
before update on public.processos_disciplinares
for each row execute function public.atualizar_processo_disciplinar_timestamp();

